	/*
	 * When decode button is pressed get .txt file and call TextWriter @Class
	 */
	private void btnOpenFileActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnOpenFileActionPerformed
		int returnValue = openFileChooser.showOpenDialog(this);
		// verify if there was a file chosen
		if (returnValue == JFileChooser.APPROVE_OPTION) {
			try {
				setButtonVisibility(false);
				// show progress bar
				setProgressBarVisibility(true);
				// set on progress bar display
				progressBar.setStringPainted(true);
				
				documentoTXT = new ArrayList<>();
				// scanner receive .txt document
				Scanner qv = new Scanner(openFileChooser.getSelectedFile());
				// insert the document lines to list
				while (qv.hasNextLine()) {
					documentoTXT.add(qv.nextLine().toUpperCase());
				}			
				// close Scanner object
				qv.close();
				setProgressBarPercentage(15);
				EscritorArquivo escritor = new EscritorArquivo();
				escritor.escreveNovoDocumento(documentoTXT);
				/** instance TextWriter class to write a new document
				EscritorTexto escritor = new EscritorTexto();
				escritor.separaInstrucao(documentoTXT);
				escritor.escrevePipelineNoDocumento();
				**/
				setProgressBarPercentage(100);
				JOptionPane.showMessageDialog(null, "Processo concluído com sucesso. Um arquivo chamado"
						+ " dados-CACHE.txt está disponível na pasta deste programa.");
				progressBar.setVisible(false);
				setButtonVisibility(true);
			} catch (IOException ioe) {
				JOptionPane.showMessageDialog(null, ioe);
			}
		} else {
			JOptionPane.showMessageDialog(null, "Por gentileza, selecione um arquivo .TXT.");
		}
	}// GEN-LAST:event_btnOpenFileActionPerformed

